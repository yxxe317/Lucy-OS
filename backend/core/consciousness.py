    async def _store_thought(self, thought: Thought):
        """Store thought in database"""
        conn = sqlite3.connect(str(self.db_path))
        cursor = conn.cursor()
        cursor.execute('''
            INSERT OR REPLACE INTO thoughts
            (id, type, content, timestamp, context, impact_score, was_shared, related_thoughts)
            VALUES (?, ?, ?, ?, ?, ?, ?, ?)
        ''', (
            thought.id,
            thought.type.value,
            thought.content,
            thought.timestamp.isoformat(),
            json.dumps(thought.context),
            thought.impact_score,
            thought.was_shared,
            json.dumps(thought.related_thoughts or [])
        ))
        conn.commit()
        conn.close()
        
        self.thought_stream.append(thought)
        if len(self.thought_stream) > 1000:
            self.thought_stream = self.thought_stream[-1000:]

