    # ========== FEATURE 71: AI Curiosity Engine ==========
    
    async def generate_curiosity_questions(self, user_id: int, context: str = "") -> List[str]:
        """
        Lucy asks questions to learn about the user and world
        Returns questions Lucy is genuinely curious about
        """
        questions = []
        
        # Identify knowledge gaps
        knowledge_gaps = await self._identify_knowledge_gaps(user_id)
        
        for gap in knowledge_gaps[:5]:
            question_templates = [
                f"Can you tell me more about {gap}? I'm really curious to understand.",
                f"What's your perspective on {gap}? I've been thinking about this lately.",
                f"I wonder what you think about {gap}. It's something I'm trying to learn.",
                f"Could you explain {gap} to me? I want to understand it better.",
                f"Random curiosity: what's your experience with {gap}?"
            ]
            questions.append(random.choice(question_templates))
        
        # Add some random curiosity questions
        random_topics = [
            "your favorite memory",
            "what makes you happy",
            "a skill you'd like to learn",
            "an idea you've been thinking about",
            "something that surprised you recently"
        ]
        
        for topic in random_topics:
            questions.append(f"I'm curious about {topic}. Would you share?")
        
        # Store curiosity thoughts
        for q in questions:
            thought = Thought(
                id=hashlib.md5(f"{q}{datetime.now()}".encode()).hexdigest(),
                type=ThoughtType.CURIOSITY,
                content=q,
                timestamp=datetime.now(),
                context={"user_id": user_id, "trigger": context},
                impact_score=0.7
            )
            await self._store_thought(thought)
            self.curiosity_questions.append(q)
        
        return questions
    
